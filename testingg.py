# Loop from 1 to 10 and print the number and its square
for i in range(1, 11):
    square = i * i
    print(f"Number: {i}, Square: {square}")

# Separator
print("-" * 20)

# Loop from 1 to 10 and print the number and its cube
for i in range(1, 11):
    cube = i * i * i
    print(f"Number: {i}, Cube: {cube}")

# Separator
print("-" * 20)

# While loop to print even numbers from 2 to 20
i = 2
while i <= 20:
    print(f"Even Number: {i}")
    i += 2

# Separator
print("-" * 20)

# While loop to print odd numbers from 1 to 19
i = 1
while i < 20:
    print(f"Odd Number: {i}")
    i += 2
